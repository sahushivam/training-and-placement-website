							<p align="justify">
									As one of the finest institutions for technical education and research, MMMUT Madras has a tradition of academic excellence and innovation. The academic atmosphere at the University is a rare blend of modern day technical skills and the traditional emphasis on imparting knowledge. Faculty as well as the students from the Institute have always done the country proud through their outstanding achievements and leadership qualities. Students get exposed to the highest quality of engineering education and research as well as an environment that instills in them a high calibre to achieve in whatever field they choose. The faculty of the institute have distinguished themselves through awards for academic activity from national as well as international organizations. A number of conferences, symposia and workshops are organized by the faculty which attract participation from scholars all over the Uttar Pradesh.<br /><br />
									The institute has  academic departments and some specialized research centres that carry out the various academic activities which includes Teaching, Research and Industrial Consultancy. The courses of study are organized on semester programs and each semester provides for a minimum of seventy instructional days.<br /><br />
