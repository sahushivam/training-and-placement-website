							<h3><font color="#003399"><b>Departmental and Institute Electives</b></font></h3> <br />
								 <p align="justify">
									The academic curriculum requires the students to opt for various elective courses within their department. This gives them a broader perspective of the various research fields in their chosen branch. Also, students can take up electives and/or audit courses from departments other than their core area of specialization. For this end, the institute has a Minor Stream program included in the curriculum wherein students can take up courses in Humanities, Management Sciences or other engineering and scientific disciplines.<br />
								 </p><br />
								 <h3><font color="#003399"><b>Co-curricular and Extra-curricular Activities</b></font></h3> <br />
								 <p align="justify">
									The rigours of academic study at each level are balanced with a number co-curricular and extra-curricular activities. Special lectures on varied topics of academic relevance are held under the <b>Extra Mural Lecture </b>series. The enterprising spirit of the students is brought to the fore in the famous annual technical festival of MMMUT Gorakhpur, <a href="http://www.shaastra.org" target="_blank"><font color="#3399FF">Abhivakti</font></a>, which has become synonymous with engineering excellence and innovation. MMMUT's annual cultural festival, <a href="http://www.saarang.org" target="_blank"><font color="#3399FF">Abhivakti</font></a> is well known for its popularity across the country. Many departments also have their own technical festivals to foster the spirit of engineering. There are also various academic hobby clubs like Robotics, Physics, Photography for students to pursue what interests them.<br />
								 </p><br />
								 <h3><font color="#003399"><b>Industry Interactions</b></font></h3> <br />
								 <p align="justify">
									MMMUT Gorakhpur has set a fine example of interaction with the industry, through consultancy services offered by the faculty. Innovative ideas are put to practice in many projects sponsored by other institutions. Also many firms have partnership agreements with the institute for joint research and development.<br />
								 </p><br />
