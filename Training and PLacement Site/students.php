		<h2>Placement Rules and Regulations</h2><br><br />
	</div>
					  <p align="justify">
					  <h3><font color="#003399"><b>A. ELIGIBILITY & REGISTRATION</b></font></h3> <br />
					  </p><p align="justify">
							1.	All students who expect to graduate from the University by the end of the academic year 2017-18 and are seeking employment may register for campus placements with this office. Placement Registration is for ONE ACADEMIC YEAR ONLY.<br /><br />
							2.	Registration for all programmes will be done during the month of August.<br /><br />
							
							3.	MS & PhD scholarsâ€™ placement will happen round the year. Their registration will be valid for one year only. They can register by attaching a certificate (NOC) & Abstract in the prescribed format available in the Placement website.<br /><br />
								
								<strong><span class="style11">Inter-disciplinary (ID) students will be permitted to upload resumes for both the departments as approved at the time of admission to the program. The NoC for ID students must be signed by the guide from both the departments.</span></strong>
							<br /><br />
							4.	Campus placement is a facility provided for the students. Registration is not compulsory. Students not interested in placement are advised not to register for placement.<br /><br />
							
							5.	<strong><b style="font-weight: 900">Backlogs:</b></strong> Students having backlog of 4 or more courses are not permitted to register for placement. Such students are advised to clear the backlogs and then register after the July-November end semester examinations. However, extended students can register in case of non-completion of course/project requirements in their last two semesters.<br /><br />
							
							6.	<b style="font-weight: 900">RE-REGISTRATION:</b> Students who have lost the job for genuine reasons will be allowed to re-register on a case to case basis.<br /><br />
							
							Students who have graduated in 2017, but not placed due to various genuine reasons, may be permitted to re-register with the approval of the concerned authorities. They will be permitted to appear for placement only from Day 3 or later, as approved by the Board of Placements.<br /><br />
							
							7.	Each registered student will be given a Placement Registration (PR) Number to access the placement notices, company announcements and to upload resumes.<br /><br />
								
							*<strong>The registration fee will not be refunded under any circumstance</strong>. <br />
							<br />
					 </p><p align="justify">
					 <h3><font color="#003399"><b>B. RESUME</b></font></h3><br />
					 </p><p align="justify">
					
						8.	Students are advised to read the announcements made, go through the company website and apply only if interested.<br /><br />
			
						9.	Students are expected to follow the institute resume template available in the placement website for preparing the resumes.<br /><br />
						
						10.	The details given in the resume have to be genuine and any student found violating this rule will be disallowed from the placement for the rest of the academic year.<br /><br />
						   
						11.	Students have to upload and submit their resumes on the website to individual companies well before the deadline. Resume once submitted cannot be modified. <b style="font-weight: 900"> DEADLINES WILL NOT BE EXTENDED UNDER ANY CIRCUMSTANCE.</b><br /><br />
						
						12.	Students are advised to avoid last minute uploading, as it may lead to delays and some may even be left out. <br /><br />
			
					 <br />
					 </p><p align="justify">
						<h3><font color="#003399"><b style="font-weight: 900">C. PRE-PLACEMENT TALKS (PPT)</b></font></h3><br /><br />
					 </p><p align="justify">
						13.	Notices of the PPT will be published in the placement website well in advance.  Students should be seated in the venue 15 minutes before the scheduled start of the PPT.<br /><br />

						14.	Students interested in a particular company, can attend its PPT.  Attendance for PPT is not compulsory.<br /><br />
						
						15.	Students <strong>must go through</strong> the complete selection process of a particular company, once their resumes are uploaded.<br /><br />
						
						16.	Any clarification regarding salary break-up, job profile, place of work, bond details etc. must be sought from the companies during PPT or interview.<br /><br />
						
						17.	<u>DRESS CODE</u>: Students <strong>must be formally dressed </strong>whenever they participate in any interaction with a company<br /><br />
						
						<strong>**This office reserves the right to refuse permission to a student to attend the selection process/PPT, if they do not dress up formally.  
						Students are expected to know the norms for formal wear; for the benefit of those who claim ignorance, please note that the following are strictly not allowed:<br /><br /></strong>

						*	T-shirts with printed text; un-collared T-shirts;<br />
						*	Shorts<br />
						*	Jeans<br />
						*	Shirt not-tucked in<br />
						*	Chappals / flip-flops<br /><br />

					 </p>
					 </p><p align="justify">
			  <h3><font color="#003399"><b>D. PLACEMENT PROCESS</b></font></h3>  <br /><br /> 
					 </p><p align="justify">
						18.	It is the responsibility of the student to check announcements / notices /  updated information / shortlisted names etc. in the notice boards of Placement Website.  Students are expected to be punctual.  <br /><br />
			
						19. <u> ATTENDANCE & PUNCTUALITY: </u><br /><br />
			
							a) A student who applies and gets shortlisted is bound to go through the entire selection process unless rejected midway by the company.  Any student who withdraws deliberately in the middle of a selection process will be disallowed from placement for the rest of the academic year.<br />  <br />
							b) <b>LATE COMERS FOR APTITUDE TEST / GD / INTERVIEW </b>may not be allowed to appear for the selection process.<br /><br />
			
						20.<u> DISCIPLINE:</u><br/><br/>
							
							a) Students should maintain discipline and show ethical behaviour in every action they take during the placement process. Any student found violating the discipline rules set by the company or defaming the institute's name will be disallowed from the placements for the rest of the academic year. <br /><br />
			
							b) Students found cheating or misbehaving in the selection process (Test / GD / Interview) will be disallowed from the placements for the rest of the academic year. <br /><br />
							
							c)	Dress code should be maintained as in #17.
					 <br /><br />
					 </p><p align="justify">
						<h3><font color="#003399"><b>E. JOB OFFERS</b></font></h3>   <br />
					 </p><p align="justify">
						21. <u>PRE PLACEMENT OFFERS:</u> <br /><br />
							The Placement Office has de-linked Pre-Placement Offers (PPO) from placements. The following rules are applicable to companies that made PPO through the Placement Office. <br /><br />
							a.	In case the student is required to make a decision on the PPO within a specified period (no longer than 10 days), then such a PPO must be made on or before 11 th September.  <br /><br />

							b.	Once a student accepts a PPO, he / she shall be de-registered from placement.<br /><br />
							
							c.	In case the PPO is an open offer, then the student will be permitted to participate in the Placement process until the day/session in which the company that has given the PPO is slotted to come to campus. Such a PPO may be offered after 11 th September also.<br /><br />
							
							d.	The offer of PPO (by the company) and its acceptance (by the student) shall be through placement office only.
							<br /><br />
							
						22. <u>MULTIPLE OFFERS:</u> Each student is eligible for only one job offer. <br /><br />
			
							a)	If a student receives more than one offer in a session/day and if there is a delay in the announcement of results by some companies, the student is bound to accept/reject the job offer of the company whose results are declared in time.<br /><br/>
							b)	If the results are declared on the same session / day, the student may choose from the offers in hand and inform the placement office of his/her choice, within 24 hrs of announcement of results.<br /><br />
			
						23.	Every student who is selected by a company is out of placement thereafter i.e. deregistered from the placement website. <br /><br />
			
						24.	<u>RELEASE OF OFFER</u>:  All companies are requested to release the Offer and hand 	over to this office within an hour after the completion of the recruitment process.<br /><br />
			
						25.	<u>Offer Acceptance</u>:  The students should inform the acceptance/rejection of offer within 24 hours (on the day following the release of offer).<br /><br />
							
							The company shall be intimated of the offer acceptance/rejection within three days of release of offer. <br /><br />
			
						26.	<u>WAITLIST</u>:  In case of those students who are placed and waitlisted by other companies, they will be given 2 days to accept the offer on hand.<br /><br/>
			
							The Placement Office in the meantime will inform the company where he is waitlisted about his present offer.<br /><br />
				
							The company that has waitlisted the students is required to release the offer within 24 hours, failing which the name of the student will be removed from the waitlist.<br /><br />
			
						27.	<u>OFFER OF JOB</u>: Announcement on the website will be considered as firm offer. Offers received from companies must be collected as per timings in circular / notice. The responsibility of going through the offer letter and taking actions therein such as submission of documents lies entirely with the student. All offers (made by the companies) shall be through this office only. This office will not be in a position to resolve problems, if any, that may arise with respect to offers made directly to the student by the company.<br /><br /> 
			
					 <br />
					 </p><p align="justify">
						<h3><font color="#003399"><b>F. MISCELLANEOUS</b></font></h3>   <br />
					 </p><p align="justify">
						29. <u>MEDICAL TEST:</u> The Placement office assumes that every selected student will pass the medical test. If there is a rejection at this stage, the student registration will be renewed and the student becomes eligible again to seek placement through this office. Students should go through and understand instructions related to medical test carefully. The same should also be adequately clarified during PPT/interview.<br /><br /> 
			
						30. <u>JOINING STATUS:</u> In case of students who go for higher studies and hence decide not to join the company, should inform the company in writing at the earliest with a copy to Placement Office.<br /><br />
			
						31. <u>IDENTITY CARDS:</u> Students <strong>must</strong> bring their identity cards with them whenever they go through a placement process.<br /><br />
			
						32. For all matters not covered by the above regulations, the Placement Office will 	use its discretion to take appropriate decisions.  The decision taken by this 	office shall be binding on all students/scholars. 
					 <br /><br />
